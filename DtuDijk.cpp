void printPath(int source, int vertex, vector<int>&predecessor, vector<string>&stations)
{
    stack<int> path;
    int crawl = vertex;
    path.push(crawl);
    while (predecessor[crawl] != -1)
    {
        path.push(predecessor[crawl]);
        crawl = predecessor[crawl];
    }

    cout << "Path from " << stations[source] << " to " << stations[vertex] << ": ";
    while (!path.empty())
    {
        cout << stations[path.top()];
        if (path.size() != 1)cout << "->";
        path.pop();
    }
    cout << endl;
}

void dijkstra(int graph[56][56], int source, vector<string>&stations)
{

    vector<int>distance(56, INT_MAX);
    vector<int>predecessor(56, -1);

    // Min-heap priority queue to store (distance, vertex)
    priority_queue<int, vector<int>, greater<int>> pq;

    distance[source] = 0;
    pq.push(source);

    while (!pq.empty())
    {
        int u = pq.top();
        pq.pop();


        for (int v = 0; v < 56; ++v)
        {
            if (graph[u][v] && distance[u] + graph[u][v] < distance[v])
            {
                distance[v] = distance[u] + graph[u][v];
                pq.push(v);
                predecessor[v] = u;
            }
        }
    }

    cout << "Minimum Number of places from " << stations[source] << " to every spot" << endl;
    for (int i = 0; i < 56; ++i)
    {
        cout << endl;
        if (distance[i] == INT_MAX)
        {
            cout << "There is no path from " << stations[source] << " to " << stations[i] << endl;
        }
        else
        {
            cout << "Minimum places from " << stations[source] << " to " << stations[i] << " are " << distance[i] << endl;
            printPath(source, i, predecessor, stations);
        }
    }
}
